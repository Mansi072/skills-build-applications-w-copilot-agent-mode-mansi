from django.core.management.base import BaseCommand
from octofit_tracker.models import User, Team, Activity, Workout, Leaderboard
from django.utils import timezone

class Command(BaseCommand):
    help = 'Populate the octofit_db database with test data'

    def handle(self, *args, **kwargs):
        # Clear existing data
        Activity.objects.all().delete()
        Leaderboard.objects.all().delete()
        Workout.objects.all().delete()
        User.objects.all().delete()
        Team.objects.all().delete()

        # Create Teams
        marvel = Team.objects.create(name='Marvel', description='Team Marvel Superheroes')
        dc = Team.objects.create(name='DC', description='Team DC Superheroes')

        # Create Users
        users = [
            User.objects.create(email='tony@stark.com', name='Tony Stark', team=marvel),
            User.objects.create(email='steve@rogers.com', name='Steve Rogers', team=marvel),
            User.objects.create(email='bruce@wayne.com', name='Bruce Wayne', team=dc),
            User.objects.create(email='clark@kent.com', name='Clark Kent', team=dc),
        ]

        # Create Workouts
        workout1 = Workout.objects.create(name='Super Strength', description='Strength training for superheroes')
        workout2 = Workout.objects.create(name='Flight Training', description='Flight skills for heroes')
        workout1.suggested_for.set(users)
        workout2.suggested_for.set(users)

        # Create Activities
        Activity.objects.create(user=users[0], activity_type='Iron Suit Training', duration_minutes=60, date=timezone.now().date())
        Activity.objects.create(user=users[1], activity_type='Shield Practice', duration_minutes=45, date=timezone.now().date())
        Activity.objects.create(user=users[2], activity_type='Detective Work', duration_minutes=90, date=timezone.now().date())
        Activity.objects.create(user=users[3], activity_type='Flying', duration_minutes=120, date=timezone.now().date())

        # Create Leaderboard
        Leaderboard.objects.create(team=marvel, total_points=200, week=timezone.now().date())
        Leaderboard.objects.create(team=dc, total_points=180, week=timezone.now().date())

        self.stdout.write(self.style.SUCCESS('octofit_db database populated with test data.'))
